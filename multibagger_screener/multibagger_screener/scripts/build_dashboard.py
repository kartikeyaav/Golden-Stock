def _market_cap(sym: str):
    """Current market cap (Cr) from the screener cache — available for the
    whole universe (fetched during the PIT backfill), so cap tiers are
    classified consistently, not just for the shortlist."""
    raw = load_company(sym)
    if not raw:
        return None
    mc = (raw.get("top_ratios") or {}).get("Market Cap")
    try:
        return float(mc) if mc is not None else None
    except (TypeError, ValueError):
        return None

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
OUT = os.path.join(ROOT, "dashboard.html")


def _read_csv(name: str) -> pd.DataFrame:
    p = os.path.join(ROOT, name)
    return pd.read_csv(p) if os.path.exists(p) else pd.DataFrame()


def _ohlc(sym: str, days: int = 260) -> list:
    df = load_ohlcv(sym)
    if df is None or len(df) < 30:
        return []
    t = df.tail(days)
    return [[str(d.date()), round(o, 2), round(h, 2), round(l, 2), round(c, 2), int(v)]
            for d, o, h, l, c, v in zip(t["date"], t["open"], t["high"],
                                        t["low"], t["close"], t["volume"])]


def _closes(sym: str, days: int = 120) -> list:
    df = load_ohlcv(sym)
    if df is None or len(df) < 20:
        return []
    return [round(float(c), 2) for c in df["close"].tail(days)]


def _fund_series(sym: str) -> dict:
    raw = load_company(sym)
    if not raw:
        return {}
    out = {}
    q = raw.get("quarters", {})
    if q.get("rows"):
        cols = q.get("columns", [])[-12:]
        np_row = (q["rows"].get("Net Profit") or [])[-12:]
        opm = (q["rows"].get("OPM %") or q["rows"].get("Financing Margin %") or [])[-12:]
        out["q_labels"] = cols
        out["np"] = np_row
        out["opm"] = opm
    bs = raw.get("balance_sheet", {})
    if bs.get("rows"):
        out["bs_labels"] = bs.get("columns", [])[-10:]
        out["debt"] = (bs["rows"].get("Borrowings") or [])[-10:]
    sh = raw.get("shareholding", {})
    if sh.get("rows"):
        out["sh_labels"] = sh.get("columns", [])[-12:]
        out["prom"] = (sh["rows"].get("Promoters") or [])[-12:]
        out["fii"] = (sh["rows"].get("FIIs") or [])[-12:]
        out["dii"] = (sh["rows"].get("DIIs") or [])[-12:]
    return out


def build_payload() -> dict:
    universe = _read_csv("universe.csv")
    focus = _read_csv("focus_list.csv")
    ranked = _read_csv("shortlist_ranked.csv")
    funds = _read_csv("fundamentals_flat.csv")
    positions = _read_csv("positions.csv")
    journal = _read_csv(os.path.join("journal", "signals_journal.csv"))
    outcomes = _read_csv(os.path.join("journal", "journal_outcomes.csv"))
    details = {}
    dpath = os.path.join(ROOT, "shortlist_details.json")
    if os.path.exists(dpath):
        details = json.load(open(dpath, encoding="utf-8"))
    # alert-time details from the nightly scan — every alerted name gets a
    # full drawer, not just the weekly shortlist; alert data is fresher so
    # it wins on collision
    adpath = os.path.join(ROOT, "state", "alert_details.json")
    if os.path.exists(adpath):
        try:
            details.update(json.load(open(adpath, encoding="utf-8")))
        except ValueError:
            pass
    ai_picks = {}
    ppath = os.path.join(ROOT, "ai_picks.json")
    if os.path.exists(ppath):
        ai_picks = json.load(open(ppath, encoding="utf-8"))
    company_by_sym = dict(zip(universe.get("symbol", []), universe.get("company", [])))

    state_path = os.path.join(ROOT, "state", "tags_state.json")
    tags = {}
    if os.path.exists(state_path):
        tags = json.load(open(state_path, encoding="utf-8")).get("tags", {})

    bench = load_ohlcv("NIFTY50")
    nifty, regime_defensive, bench_age = [], False, 99
    if bench is not None and len(bench) > 160:
        b = bench.tail(500).copy()
        b["sma"] = bench["close"].rolling(150).mean().tail(500)
        nifty = [[str(d.date()), round(float(c), 1),
                  round(float(s), 1) if pd.notna(s) else None]
                 for d, c, s in zip(b["date"], b["close"], b["sma"])]
        regime_defensive = bool(b["close"].iloc[-1] < b["sma"].iloc[-1])
        bench_age = (datetime.now() - bench["date"].iloc[-1]).days

    # alerts + verdicts
    alerts, verdicts, scan_date = [], "", ""
    ap = os.path.join(ROOT, "daily_alerts.md")
    if os.path.exists(ap):
        raw = open(ap, encoding="utf-8").read()
        m = re.search(r"Daily scan . (.+)", raw)
        scan_date = m.group(1).strip() if m else ""
        alerts = [{"kind": k, "text": v} for k, v in
                  re.findall(r"^- \*\*(.+?)\*\*: (.+)$", raw, re.M)]
        vm = re.search(r"## AI analyst verdicts\n(.*?)(?=\n## |\Z)", raw, re.S)
        verdicts = vm.group(1).strip()[:4000] if vm else ""

    # screener rows: focus list enriched with fundamentals where known
    fund_by_sym = {r["symbol"]: r for _, r in funds.iterrows()} if not funds.empty else {}
    score_by_sym = dict(zip(ranked.get("symbol", []), ranked.get("score", [])))
    veto_by_sym = dict(zip(ranked.get("symbol", []), ranked.get("vetoed", [])))
    arch_by_sym = dict(zip(ranked.get("symbol", []), ranked.get("archetypes", [])))
    TIER = {"microcap250": "Micro", "smallcap250": "Small", "midcap150": "Mid"}

    def cap_tier(mcap, idx_src):
        # actual market cap wins; index membership is the fallback label
        if mcap is not None and pd.notna(mcap):
            m = float(mcap)
            return "Micro" if m < 2000 else "Small" if m < 12000 else "Mid" if m < 50000 else "Large"
        return TIER.get(str(idx_src), "")

    screener_rows, closes = [], {}
    for _, r in focus.iterrows():
        sym = r["symbol"]
        f = fund_by_sym.get(sym, {})
        arch = str(arch_by_sym.get(sym, ""))
        # market cap from the full-universe screener cache (consistent), with
        # the shortlist flat file as a secondary source
        mcap = _market_cap(sym)
        if mcap is None:
            mcap = f.get("market_cap_cr")
        closes[sym] = _closes(sym)
        # price from tonight's cache — the focus snapshot's last_close is a
        # WEEKLY artifact and goes stale within a day (user-caught 2026-07-09)
        live_close = closes[sym][-1] if closes[sym] else (
            round(float(r["last_close"]), 2) if pd.notna(r.get("last_close")) else None)
        screener_rows.append({
            "sym": sym, "company": str(company_by_sym.get(sym, ""))[:40],
            "ind": str(r.get("industry", ""))[:30],
            # tonight's state wins over the weekly focus snapshot (freshness)
            "tag": tags.get(sym, r.get("tag", "")),
            "tier": cap_tier(mcap, r.get("index_source", "")),
            "mcap": round(float(mcap), 0) if mcap is not None and pd.notna(mcap) else None,
            "rs": round(float(r["rs_pctile"]), 1) if pd.notna(r.get("rs_pctile")) else None,
            "close": live_close,
            "turn": round(float(r["turnover_cr"]), 1) if pd.notna(r.get("turnover_cr")) else None,
            "score": round(float(score_by_sym[sym]), 1) if sym in score_by_sym and pd.notna(score_by_sym[sym]) else None,
            "veto": bool(veto_by_sym.get(sym, False)),
            "arch": "" if "untagged" in arch else arch[:26],
            "roce": f.get("roce_pct"), "pe": f.get("pe"),
            "pgttm": f.get("profit_growth_ttm"),
            "focus": True,
        })

    # the scan watches (and alerts from) the WHOLE tagged universe — the
    # screener must show those names too, or alerted stocks are unfindable
    # (user-caught 2026-07-10). Non-focus names carry no RS percentile (that
    # is a focus-list artifact) but get tag/price/cap/score like everyone.
    ind_by_sym = dict(zip(universe.get("symbol", []), universe.get("industry", [])))
    focus_syms = set(focus["symbol"]) if not focus.empty else set()
    for sym, tg in tags.items():
        if sym in focus_syms:
            continue
        f = fund_by_sym.get(sym, {})
        closes[sym] = _closes(sym)
        live_close = closes[sym][-1] if closes[sym] else None
        mcap = _market_cap(sym)
        if mcap is None:
            mcap = f.get("market_cap_cr")
        arch = str(arch_by_sym.get(sym, ""))
        screener_rows.append({
            "sym": sym, "company": str(company_by_sym.get(sym, ""))[:40],
            "ind": str(ind_by_sym.get(sym, ""))[:30],
            "tag": tg,
            "tier": cap_tier(mcap, ""),
            "mcap": round(float(mcap), 0) if mcap is not None and pd.notna(mcap) else None,
            "rs": None,
            "close": live_close,
            "turn": None,
            "score": round(float(score_by_sym[sym]), 1) if sym in score_by_sym and pd.notna(score_by_sym[sym]) else None,
            "veto": bool(veto_by_sym.get(sym, False)),
            "arch": "" if "untagged" in arch else arch[:26],
            "roce": f.get("roce_pct"), "pe": f.get("pe"),
            "pgttm": f.get("profit_growth_ttm"),
            "focus": False,
        })

    # detail data: OHLC + fundamentals for shortlist + positions + paper book
    # + every recently-alerted name (details now includes alert_details.json)
    paper_pos = _read_csv("paper_positions.csv")
    detail_syms = (list(ranked.get("symbol", [])) + list(positions.get("symbol", []))
                   + list(paper_pos.get("symbol", [])) + list(details.keys()))
    ohlc = {s: _ohlc(s) for s in dict.fromkeys(detail_syms)}
    fund_series = {s: _fund_series(s) for s in dict.fromkeys(detail_syms)}

    pos_rows = []
    for _, p in positions.iterrows():
        df = load_ohlcv(p["symbol"])
        last = float(df["close"].iloc[-1]) if df is not None else float(p["entry_price"])
        risk = float(p["entry_price"]) - float(p["initial_stop"])
        pos_rows.append({
            "sym": p["symbol"], "entry": float(p["entry_price"]),
            "stop": float(p["stop_current"]), "last": round(last, 2),
            "be": round(float(p["entry_price"]) + 1.5 * risk, 2),
            "partial": round(float(p["entry_price"]) + 2.5 * risk, 2),
            "shares": int(p["shares_trading"]) + int(p["shares_core"]),
            "pnl": round((last / float(p["entry_price"]) - 1) * 100, 2),
            "notes": str(p.get("notes", "")),
        })

    j_rows = []
    journal_total = 0 if journal.empty else len(journal)
    if not journal.empty:
        for _, r in journal.tail(50).iloc[::-1].iterrows():
            j_rows.append({"d": str(r["logged_at"])[:16], "sym": r["symbol"],
                           "kind": r["kind"], "old": str(r.get("old_tag", "")),
                           "new": str(r.get("new_tag", ""))[:40]})
    out_stats = {}
    if not outcomes.empty:
        rvals = pd.to_numeric(outcomes.get("r_to_date"), errors="coerce").dropna()
        out_stats = {"n": len(outcomes),
                     "open": int((outcomes["status"] == "open").sum()),
                     "stopped": int((outcomes["status"] == "stopped").sum()),
                     "exp": round(float(rvals.mean()), 2) if len(rvals) else None}

    # journal scorecard: every buy-type signal marked to market (Journal tab)
    def _num(v, nd=2):
        v = pd.to_numeric(pd.Series([v]), errors="coerce").iloc[0]
        return round(float(v), nd) if pd.notna(v) else None
    score_rows = []
    if not outcomes.empty:
        for _, r in outcomes.iloc[::-1].iterrows():
            score_rows.append({
                "d": str(r["logged_at"])[:16], "sym": r["symbol"],
                "kind": str(r.get("kind", "")), "entry": _num(r.get("close")),
                "stop": _num(r.get("stop_suggested")),
                "ret": _num(r.get("return_to_date_pct"), 1),
                "r": _num(r.get("r_to_date")), "maxr": _num(r.get("max_favorable_R")),
                "status": str(r.get("status", "")),
                "conv": _num(r.get("conviction_score"), 0)})

    # "actionable now" — buy signals from the last 7 days, marked against
    # tonight's tag + price so fresh/extended/faded is explicit (the
    # demarcation the alert stream alone doesn't give)
    verdict_by_sym = {}
    vpath = os.path.join(ROOT, "journal", "analyst_verdicts.csv")
    if os.path.exists(vpath):
        vs = pd.read_csv(vpath)
        for _, v in vs.iterrows():
            verdict_by_sym[v["symbol"]] = (f"{v['verdict']}/{v.get('conviction', '')}"
                                           f"/{v.get('size', '')}")
    # entry-fidelity labels per buy alert (journal/entry_signals.csv) — shows
    # whether an alert was the exact backtested trigger or a watch-the-pivot
    trigger_by_sym = {}
    espath = os.path.join(ROOT, "journal", "entry_signals.csv")
    if os.path.exists(espath):
        es = pd.read_csv(espath)
        es["logged_at"] = pd.to_datetime(es["logged_at"], errors="coerce")
        for _, r in es[es["logged_at"] >= pd.Timestamp.now() - pd.Timedelta(days=7)].iterrows():
            trigger_by_sym[r["symbol"]] = str(r.get("entry_status", ""))

    actionable = []
    if not journal.empty:
        jj = journal.copy()
        jj["logged_at"] = pd.to_datetime(jj["logged_at"], errors="coerce")
        recent = jj[(jj["kind"].isin(["BUY CANDIDATE", "RE-ENTRY WINDOW"]))
                    & (jj["logged_at"] >= pd.Timestamp.now() - pd.Timedelta(days=7))]
        for _, r in recent.iloc[::-1].iterrows():
            sym = r["symbol"]
            df = load_ohlcv(sym)
            now_px = round(float(df["close"].iloc[-1]), 2) if df is not None and len(df) else None
            alert_px = _num(r.get("close"))
            tag_now = tags.get(sym, "")
            vetoed = str(r.get("vetoed", "")).lower() == "true"
            if vetoed:
                status = "VETOED"
            elif tag_now == "CONFIRMED":
                status = "ACTIONABLE"
            elif tag_now == "EXTENDED":
                status = "RAN AWAY"
            else:
                status = "FADED"
            # conviction: the alert-night number; older journal rows that
            # predate a card fall back to the weekly shortlist score
            conv = _num(r.get("conviction_score"), 0)
            if conv is None and sym in score_by_sym and pd.notna(score_by_sym[sym]):
                conv = round(float(score_by_sym[sym]), 0)
            actionable.append({
                "sym": sym, "d": f"{r['logged_at']:%d %b}", "kind": str(r["kind"]),
                "alert_px": alert_px, "now_px": now_px,
                "chg": round((now_px / alert_px - 1) * 100, 1) if now_px and alert_px else None,
                "conv": conv, "tag": tag_now,
                "trigger": trigger_by_sym.get(sym, ""),
                "verdict": verdict_by_sym.get(sym, ""), "status": status})

    # decision-first ordering + a plain-English instruction per row (the
    # panel is the action surface — it must answer "so what do I DO?")
    def _do(a):
        if a["status"] == "VETOED":
            return "DO NOT BUY", "veto"
        if a["status"] == "FADED":
            return "IGNORE — setup gone", "mute"
        if a["status"] == "RAN AWAY":
            return "WAIT — re-entry alert will fire", "warn"
        if a["trigger"] == "VALIDATED":
            return "BUY SETUP — plan in drawer", "act"
        if a["trigger"] == "AWAITING TRIGGER":
            return "WATCH — buy only on pivot breakout", "watch"
        return "WEAK — trend only, no proven trigger", "weak"
    _srank = {"ACTIONABLE": 0, "RAN AWAY": 1, "FADED": 2, "VETOED": 3}
    _trank = {"VALIDATED": 0, "AWAITING TRIGGER": 1, "NO VCP BASE": 2, "": 3}
    for a in actionable:
        a["do"], a["dokind"] = _do(a)
    actionable.sort(key=lambda a: (_srank.get(a["status"], 9),
                                   _trank.get(a["trigger"], 9),
                                   -(a["conv"] or 0)))

    try:
        paper = paper_summary()
    except Exception:  # noqa: BLE001 — dashboard must build even if paper book breaks
        paper = {}

    equity = []
    eq = _read_csv("baseline_equity.csv")
    if not eq.empty:
        eq = eq.iloc[::5]
        equity = [[str(pd.Timestamp(d).date()), round(float(v), 0)]
                  for d, v in zip(eq["date"], eq["equity"])]

    tag_counts = pd.Series(list(tags.values())).value_counts().to_dict() if tags else {}
    heat = []
    if not focus.empty:
        g = focus.groupby("industry")["rs_pctile"].agg(["mean", "count"])
        g = g[g["count"] >= 3].sort_values("mean", ascending=False)
        heat = [{"ind": str(i)[:24], "rs": round(float(r["mean"]), 0), "n": int(r["count"])}
                for i, r in g.iterrows()][:18]

    matrix = [
        {"name": "SZ2-B equity-basis sizing (ADOPTED)", "exp": 1.67, "keep": True},
        {"name": "SZ2-B deployment stress", "exp": 1.10, "keep": True},
        {"name": "A technical baseline", "exp": 1.27, "keep": True},
        {"name": "F2 fund lot-split", "exp": 1.27, "keep": False},
        {"name": "F1 core patience", "exp": 1.25, "keep": False},
        {"name": "S2 heavy costs (stress)", "exp": 1.20, "keep": True},
        {"name": "S1 next-open fills (stress)", "exp": 0.96, "keep": True},
        {"name": "B3 fund gate 0.50", "exp": 0.54, "keep": False},
        {"name": "V3a anticipation+fund", "exp": 0.41, "keep": False},
        {"name": "B1 fund gate 0.55", "exp": 0.39, "keep": False},
        {"name": "D fund entry-ranking", "exp": 0.38, "keep": False},
        {"name": "E40 sector-heat gate", "exp": 0.22, "keep": False},
        {"name": "E60 sector-heat gate", "exp": 0.11, "keep": False},
        {"name": "V3a anticipation price-only", "exp": 0.06, "keep": False},
        {"name": "SZ 16 slots (sizing matrix)", "exp": 1.15, "keep": False},
        {"name": "SZ 20 slots (sizing matrix)", "exp": 0.89, "keep": False},
    ]

    return {
        "generated": datetime.now().strftime("%d %b %Y, %H:%M"),
        "price_date": str(bench["date"].iloc[-1].date()) if bench is not None and len(bench) else "",
        "scan_date": scan_date, "defensive": regime_defensive,
        "health_ok": bench_age <= 5 and len(tags) > 400,
        "funnel": [
            ["NSE index universe", len(universe), "official constituents, refreshed weekly"],
            ["Watched nightly", len(tags), "enough history for a mechanical read"],
            ["Focus list", len(focus), "top relative strength (reporting)"],
            ["Actionable tags", tag_counts.get("CONFIRMED", 0) + tag_counts.get("ANTICIPATION", 0),
             "CONFIRMED + ANTICIPATION today"],
            ["Alerts tonight", len([a for a in alerts if a["kind"] != "POSITION"]),
             "state transitions only"],
        ],
        "tags": tag_counts, "alerts": alerts, "verdicts": verdicts, "ai_picks": ai_picks,
        "rows": screener_rows, "closes": closes, "ohlc": ohlc, "details": details,
        "fund": fund_series, "positions": pos_rows, "journal": j_rows,
        "journal_total": journal_total, "scorecard": score_rows,
        "actionable": actionable, "paper": paper,
        "outcomes": out_stats, "equity": equity, "nifty": nifty,
        "heat": heat, "matrix": matrix,
        # sizing matrix v2 (2026-07-12): equity-basis sizing adopted — the old
        # 21.5% figure was a cash-basis measurement artifact. Ideal-fill /
        # stressed pairs shown; stress = next-open fills + gap stops + costs.
        "kpi": {"exp": "+1.67R", "cagr": "47% / 33%", "dd": "-18.5 / -20.7%", "payoff": "9.6:1"},
    }


def build() -> None:
    payload = json.dumps(build_payload(), separators=(",", ":"), default=str)
    html = TEMPLATE.replace("%%PAYLOAD%%", payload)
    with open(OUT, "w", encoding="utf-8") as f:
        f.write(html)
    print(f"dashboard -> {OUT}  ({os.path.getsize(OUT) / 1e6:.1f} MB)")


