"""
scripts/build_dashboard.py — v2: a full command-center SPA in one generated
file. Tabs: Overview / Screener / Positions / Journal / Validation, plus a
click-through stock drawer with real candlestick charts (TradingView
lightweight-charts via CDN), moving averages, volume, plan price-lines,
quarterly-profit bars, debt and shareholding trends.

All data is embedded at build time (~3MB JSON): OHLC for the shortlist +
positions, close series for the whole focus list, fundamental series from
the screener cache, journal, baseline equity curve, matrix verdicts.
Regenerated by the daily and weekly jobs. Open dashboard.html in a browser.
"""

